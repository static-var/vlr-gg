@Composable
fun VLRTheme(
  darkTheme: Boolean = isSystemInDarkTheme(),
  dynamicColor: Boolean = true,
  content: @Composable () -> Unit
) {
  val colorScheme =
    when {
      dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
        val context = LocalContext.current
        if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
      }
      darkTheme -> DarkColorScheme
      else -> LightColorScheme
    }

  //  val fontFamilyResolver = LocalFontFamilyResolver.current
  //  LaunchedEffect(Unit) {
  //    fontFamilyResolver.resolve(appFontFamily)
  //  }

  MaterialTheme(
    colorScheme = colorScheme,
    typography = Typography,
    shapes = Shapes,
    content = content
  )
}

@Composable
fun WidgetTheme(
  context: Context,
  darkTheme: Boolean = isSystemInDarkTheme(),
  dynamicColor: Boolean = true,
  content: @Composable () -> Unit
) {
  val colorScheme =
    when {
      dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
        if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
      }
      darkTheme -> DarkColorScheme
      else -> LightColorScheme
    }

  MaterialTheme(
    colorScheme = colorScheme,
    typography = Typography,
    shapes = Shapes,
    content = content
  )
}

const val LIGHT_THEME_ALPHA = 0.2f
const val DARK_THEME_ALPHA = 0.05f

val VLRTheme
  @Composable get() = MaterialTheme

val ColorScheme.tintedBackground: Color
  @Composable
  get() = primaryContainer.copy(if (isSystemInDarkTheme()) DARK_THEME_ALPHA else LIGHT_THEME_ALPHA)

val ColorScheme.elevatedSurfaceColor: Color
  @Composable
  get() = VLRTheme.colorScheme.surfaceColorAtElevation(8.dp)

val ColorScheme.transparent: Color
  @Composable
  get() = Color.Transparent